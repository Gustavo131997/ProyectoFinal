    /** 
     *Este metodo establece el formato a la jTable , agregando las columnas y los atributos de la clase Itinerario a la jTable   
     **/
    protected final void listarRegistro(){
        DefaultTableModel dt = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        dt.addColumn("Codigo del Bus");
        dt.addColumn("Origen");
        dt.addColumn("Destino");
        dt.addColumn("Hora de Salida");
        dt.addColumn("Hora de Llegada");
        dt.addColumn("Fecha");
        dt.addColumn("Estado");
        
//        tabla.setDefaultRenderer(Object.class, new imgTabla());
        
        Object fila[] = new Object[dt.getColumnCount()];
        for(int i = 0; i < terminal.empresa.cantidadRegistroItinerario(); i++){
            itinerario = terminal.empresa.obtenerRegistroItinerario(i);
            fila[0] = itinerario.getBus().getCodigo();
            fila[1] = itinerario.getOrigen();
            fila[2] = itinerario.getDestino();
            fila[3] = itinerario.getHoraLlegada();
            fila[4] = itinerario.getHoraSalida();
            fila[5] = itinerario.getFecha();
            fila[6] = itinerario.getBus().getEstado();
            dt.addRow(fila);
        }
        tabla.setModel(dt);
        tabla.setRowHeight(60);
    }
    
    
