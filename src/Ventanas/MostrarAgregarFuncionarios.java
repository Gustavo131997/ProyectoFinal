    public final void listarRegistro(){
        DefaultTableModel modelo = new DefaultTableModel(){//objeto que modifica 
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        modelo.addColumn("Identificador");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Empresa");
        modelo.addColumn("Nombre de Usuario");       
        
 
        
        Object fila[] = new Object[modelo.getColumnCount()];
        
        for (int i = 0; i < terminal.empresa.getTrabajadores().size(); i++) {
            funcionario = terminal.empresa.getTrabajadores().get(i);
            if(funcionario.getRol() == 1){
            fila[0]= funcionario.getIdentificadorAleatorio();
            fila[1]= funcionario.getNombre();
            fila[2]= funcionario.getApellido();
            fila[3]= "Turbus";
            fila[4]= funcionario.getNombreusuario();
            modelo.addRow(fila);
            }
        }
        
        tabla.setModel(modelo);
        tabla.setRowHeight(60);
    }
    
    
    
