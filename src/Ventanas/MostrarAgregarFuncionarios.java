    private void ingresarRegistroFuncionario(int id, String nombre, String apellido, String nombreEmpresa, String usuario,
            String contraseña, int rol , Empresa empresa){
       
            if(id != -1 && id<0)mensaje("Ingresar codigo entero");
            else if(nombre == null)mensaje("Ingresar Nombre");
            else if(apellido == null) mensaje("Ingresar Apellido");
            else if(nombreEmpresa == null)mensaje("Ingresar Nombre de Empresa");
            else if(usuario== null)mensaje("Ingresar Usuario");
            else if(contraseña== null)mensaje("Ingresar Contraseña");
            else{
                empresa.admin = new Funcionario(id, nombre, apellido, usuario, contraseña, rol);
                if(empresa.buscaCodigoFuncionario(empresa.getAdmin().getIdentificadorAleatorio())!= -1)mensaje("Este codigo ya existe");
                else empresa.agregarRegistroFuncionario(empresa.admin);
                funcionario.getArchivo().grabar_Funcionario_txt(empresa.admin,empresa);
                listarRegistro();
                LimpiarCampos();
            }
            
    }
    
    
