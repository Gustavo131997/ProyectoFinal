    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        
        if (terminal.validarUsuarios(usuario.getText(),new String(password.getPassword()).trim().replace(" ", "_"), 1) != -1) {
               //Abrir menu de un funcionario comun
                MenuFuncionario menuFuncionario = new MenuFuncionario();
                menuFuncionario.setVisible(true);
                dispose();
                
        }else {
            if (terminal.validarUsuarios(usuario.getText(),new String(password.getPassword()).trim().replace(" ", "_"), 0) != -1) {
             
                //Abrir menu del administrador
            MenuFuncionarioAdmin menuAdmin = new MenuFuncionarioAdmin();
            menuAdmin.setVisible(true);
            dispose();
            
            }else{
                JOptionPane.showMessageDialog(null, "Usuario y/o contrase√±a incorrectos");
            }
        }
    }//GEN-LAST:event_ingresarActionPerformed

